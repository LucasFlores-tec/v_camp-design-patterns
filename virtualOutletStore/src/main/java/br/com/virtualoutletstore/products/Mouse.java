package br.com.virtualoutletstore.products;

public class Mouse {
	
	private String brand;
	
	private String type;

	public Mouse(String brand, String type) {
		this.brand = brand;
		this.type = type;
	}

	public String getBrand() {
		return brand;
	}

	public String getType() {
		return type;
	}
	
}
