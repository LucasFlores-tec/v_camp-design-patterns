package br.com.virtualoutletstore.products;

public class Tshirt {
	
	private String color;
	
	private String size;

	public Tshirt(String color, String size) {
		this.color = color;
		this.size = size;
	}

	public String getColor() {
		return color;
	}

	public String getSize() {
		return size;
	}
	
}
